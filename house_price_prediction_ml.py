# ── Feature Engineering Function ──────────────────────────────────────────────
def engineer_features(df_raw: pd.DataFrame) -> pd.DataFrame:
    """Apply the same feature engineering as training."""
    df_out = df_raw.copy()
    df_out['RoomsPerHousehold']      = df_out['AveRooms'] / df_out['AveOccup']
    df_out['BedroomRatio']           = df_out['AveBedrms'] / df_out['AveRooms']
    df_out['PopulationPerHousehold'] = df_out['Population'] / df_out['AveOccup']
    df_out['LogPopulation']          = np.log1p(df_out['Population'])
    df_out['LogAveOccup']            = np.log1p(df_out['AveOccup'])
    df_out['MedInc_sq']              = df_out['MedInc'] ** 2
    df_out['IsCoastal']              = (df_out['Longitude'] < -121.5).astype(int)
    return df_out


def predict_house_price(input_dict: dict) -> dict:
    """
    Predict house price using the tuned Random Forest model.

    Parameters:
        input_dict : raw feature values (California Housing feature names)

    Returns:
        dict with predicted price and ±RMSE confidence band
    """
    raw_df  = pd.DataFrame([input_dict])
    eng_df  = engineer_features(raw_df)
    pred    = best_rf.predict(eng_df[X.columns])[0]
    lower   = max(0, pred - rmse_final)
    upper   = pred + rmse_final

    return {
        'predicted_price_100k' : round(pred, 4),
        'predicted_price_usd'  : f'${pred * 100_000:,.0f}',
        'confidence_band_usd'  : f'${lower*100_000:,.0f} – ${upper*100_000:,.0f}'
    }


