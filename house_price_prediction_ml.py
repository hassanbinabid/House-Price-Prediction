"""## 5. 🤖 Model Training"""

# ── Helper: Evaluate any model ────────────────────────────────────────────────
def evaluate_model(model, X_tr, X_te, y_tr, y_te, name):
    """
    Trains a model and returns a dict of evaluation metrics.

    Metrics:
      R²   — proportion of variance explained (higher is better, max=1)
      RMSE — root mean squared error in $100k units
      MAE  — mean absolute error in $100k units
      MAPE — mean absolute percentage error (%)
    """
    model.fit(X_tr, y_tr)
    preds = model.predict(X_te)

    mae  = mean_absolute_error(y_te, preds)
    rmse = np.sqrt(mean_squared_error(y_te, preds))
    r2   = r2_score(y_te, preds)
    mape = mean_absolute_percentage_error(y_te, preds) * 100

    return {
        'Model'    : name,
        'R² Score' : round(r2,   4),
        'RMSE'     : round(rmse, 4),
        'MAE'      : round(mae,  4),
        'MAPE (%)' : round(mape, 2),
        '_model'   : model         # stored privately for later use
    }

